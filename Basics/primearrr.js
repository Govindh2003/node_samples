


function isprime(num){
    



  for (let i = 1; i<=1; i++) {
    if (num % i === 0) {
      return false
     
    }
    else{
        return true
        
    }
}
}

function prime(){
let arr=[12,3,4,5,7,8,11]

for(let i=0;i<arr.length;i++){
    if(isprime(arr[i])){
        console.log("IT IS A PRIME NUMBER")
    }
    else{
        console.log("NOT A PRIME")
    }
}

}
